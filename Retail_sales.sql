-- Calculate the absolute gross profit dollars and the percentage gross profit margin
-- ($\text{GPM}$) for each product category to evaluate bottom-line efficiency.
SELECT
		category,
		sum(total_sale) as revenue,
		sum(cogs) as cost_of_goods,
		sum(total_sale) - sum(cogs) as gross_profit,
		ROUND((sum(total_sale) - sum(cogs))/ sum(total_sale)*100,2) as gross_profit_margin
	from retail_sales
	where total_sale IS NOT NULL AND cogs IS NOT NULL
	group by category
	order by gross_profit_margin desc;
	 